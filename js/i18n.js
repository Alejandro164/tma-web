let currentLang = 'es';

window.i18nInit = function() {
    const savedLang = localStorage.getItem('itek_lang');
    if (savedLang && (savedLang === 'es' || savedLang === 'en')) {
        currentLang = savedLang;
    }
    applyTranslations();
    setupDynamicToggle();
}

function setupDynamicToggle() {
    const toggleBtn = document.getElementById('lang-toggle');
    if (toggleBtn) {
        bindToggle(toggleBtn);
        return;
    }
    const observer = new MutationObserver((mutations, obs) => {
        const btn = document.getElementById('lang-toggle');
        if (btn) {
            bindToggle(btn);
            applyTranslations();
            obs.disconnect();
        }
    });
    observer.observe(document.body, { childList: true, subtree: true });
}

function bindToggle(btn) {
    btn.addEventListener('click', (e) => {
        e.preventDefault();
        currentLang = currentLang === 'es' ? 'en' : 'es';
        localStorage.setItem('itek_lang', currentLang);
        applyTranslations();
    });
}

function applyTranslations() {
    document.documentElement.lang = currentLang;
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        const prop = el.getAttribute('data-i18n-prop');
        if (translations[currentLang] && translations[currentLang][key]) {
            if (prop) {
                el.setAttribute(prop, translations[currentLang][key]);
            } else {
                el.innerHTML = translations[currentLang][key];
            }
        }
    });
    const toggleBtn = document.getElementById('lang-toggle');
    if (toggleBtn) {
        toggleBtn.innerHTML = currentLang === 'es' ? 'EN' : 'ES';
        toggleBtn.setAttribute('title', currentLang === 'es' ? 'Switch to English' : 'Cambiar a Español');
    }
}
